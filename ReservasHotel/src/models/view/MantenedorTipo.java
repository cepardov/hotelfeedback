    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nombre= txtnombre.getText();
        String descripcion= txtdescripcion.getText();
        int precio=Integer.parseInt(txtprecio.getText());
        
        
        //Tipobeans t=new Tipobeans();
        t.setNombre(nombre);
        t.setDescripcion(descripcion);
        t.setPrecio(precio);
        t.save();
        this.updateTabla();
       
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
         fila = tabla.rowAtPoint(evt.getPoint());
        if (fila > -1){
            idtipo=Integer.parseInt(String.valueOf((tabla.getValueAt(fila, 0))));
            txtnombre.setText(String.valueOf(tabla.getValueAt(fila, 1)));
            txtdescripcion.setText(String.valueOf(tabla.getValueAt(fila, 2)));
            txtprecio.setText(String.valueOf(tabla.getValueAt(fila, 3)));
        }
    }//GEN-LAST:event_tablaMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String nombre= txtnombre.getText();
        String descripcion= txtdescripcion.getText();
        int precio=Integer.parseInt(txtprecio.getText());
        t.setIdtipo(idtipo);
        t.setNombre(nombre);
        t.setDescripcion(descripcion);
        t.setPrecio(precio);
        t.update();
        this.updateTabla();
    }//GEN-LAST:event_jButton2ActionPerformed

